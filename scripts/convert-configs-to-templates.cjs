const fs = require('fs');
const path = require('path');

const configsDir = path.join(__dirname, '..', 'saved-configs');
const outputFile = path.join(__dirname, '..', 'ui', 'app', 'constants', 'initialTemplates.ts');

// Read all config files, excluding personal test configs (config-1*******.json pattern)
const files = fs.readdirSync(configsDir)
  .filter(f => f.endsWith('.json'))
  .filter(f => !f.match(/^config-1\d+\.json$/));

const templates = files.map((filename, index) => {
  const filepath = path.join(configsDir, filename);
  const config = JSON.parse(fs.readFileSync(filepath, 'utf8'));
  
  // Extract name from filename (e.g., "config-retail-purchase-journey.json" -> "Retail Purchase Journey")
  const nameFromFile = filename
    .replace('config-', '')
    .replace('.json', '')
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
  
  // Get a better template name from config data or filename
  let templateName = nameFromFile;
  if (config.journeyType && config.journeyType !== nameFromFile) {
    templateName = config.journeyType;
  }
  if (config.journeyDetail && config.journeyDetail.length > 3) {
    templateName = config.journeyDetail;
  }
  
  // Clean up requirements
  let requirements = '';
  if (config.journeyRequirements) {
    // Extract just the first line or key info
    const firstLine = config.journeyRequirements.split('\\n')[0];
    requirements = firstLine.replace('Journey Type: ', '').substring(0, 150);
  } else if (config.industryType && config.journeyType) {
    requirements = `${config.industryType} - ${config.journeyType}`;
  } else {
    requirements = config.journeyDetail || config.journeyType || nameFromFile;
  }
  
  // Create template object
  const template = {
    id: `template_${Date.now() + index}`,
    name: templateName,
    companyName: config.companyName || 'Unknown',
    domain: config.domain || '',
    requirements: requirements.trim(),
    csuitePrompt: '', // Will be generated on-demand
    journeyPrompt: '', // Will be generated on-demand
    originalConfig: config, // Store full config for loading into response field
    createdAt: config.journeyStartTime || new Date().toISOString(),
    isPreloaded: true, // Mark as pre-loaded template
  };
  
  return template;
});

// Generate TypeScript file
const tsContent = `/**
 * Initial Templates - Pre-populated from saved-configs
 * Auto-generated by scripts/convert-configs-to-templates.js
 */

export interface InitialTemplate {
  id: string;
  name: string;
  companyName: string;
  domain: string;
  requirements: string;
  csuitePrompt: string;
  journeyPrompt: string;
  originalConfig?: any; // Full config JSON for pre-loaded templates
  createdAt: string;
  isPreloaded?: boolean;
}

export const INITIAL_TEMPLATES: InitialTemplate[] = ${JSON.stringify(templates, null, 2)};

export const getInitialTemplates = (): InitialTemplate[] => {
  return INITIAL_TEMPLATES;
};
`;

fs.writeFileSync(outputFile, tsContent, 'utf8');

console.log(`✅ Converted ${templates.length} configs to templates`);
console.log(`📄 Output file: ${outputFile}`);
console.log(`\n📊 Templates by company:`);

const byCompany = templates.reduce((acc, t) => {
  acc[t.companyName] = (acc[t.companyName] || 0) + 1;
  return acc;
}, {});

Object.keys(byCompany).sort().forEach(company => {
  console.log(`  ${company}: ${byCompany[company]}`);
});
